async function main() {
  const check = process.argv.includes("--check");
  let drift = false;

  for (const config of PACKAGES) {
    const outputs = [
      {
        target: path.join(config.targetDirectory, "runtime-loader.ts"),
        content: renderTypeScriptLoader(config),
      },
      {
        target: path.join(config.targetDirectory, "runtime-loader.js"),
        content: renderJavaScriptLoader(config),
      },
    ];

    for (const output of outputs) {
      if (check) {
        let current = "";
        try {
          current = await readFile(output.target, "utf8");
        } catch {
          current = "";
        }
        if (current !== output.content) {
          drift = true;
          console.error(`Out of sync: ${path.relative(repoRoot, output.target)}`);
        }
      } else {
        await writeFile(output.target, output.content);
        console.log(`Wrote ${path.relative(repoRoot, output.target)}`);
      }
    }
  }

  if (check && drift) {
    console.error("Run: node scripts/gen-package-runtime-loaders.mjs");
    process.exit(1);
  }
  if (check) {
    console.log("Package runtime loaders in sync.");
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
