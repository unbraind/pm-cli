#!/usr/bin/env node
/**
 * Generator for standalone bundled-package runtime loaders.
 *
 * CONSTRAINT: package extension entrypoints are copied into project-local pm
 * extension directories and must stay loadable without a static SDK import at
 * module evaluation time. Each generated loader is local to its package and
 * resolves that package's runtime.ts from PM_CLI_PACKAGE_ROOT, checkout roots,
 * or the copied extension directory. Extensions are authored and loaded as
 * TypeScript (Node native type stripping, Node >=22.18), so the loader is a
 * single `.ts` artifact that resolves the sibling `runtime.ts` — there is no
 * compiled `.js` counterpart.
 *
 * Usage:
 *   node scripts/gen-package-runtime-loaders.mjs          # write loaders
 *   node scripts/gen-package-runtime-loaders.mjs --check  # fail on drift
 */
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const PACKAGES = [
  {
    extensionName: "beads",
    packageName: "pm-beads",
    diagnosticName: "beads",
    template: "standard",
    targetDirectory: path.join(repoRoot, "packages", "pm-beads", "extensions", "beads"),
  },
  {
    extensionName: "todos",
    packageName: "pm-todos",
    diagnosticName: "todos",
    template: "set-based",
    targetDirectory: path.join(repoRoot, "packages", "pm-todos", "extensions", "todos"),
  },
];

function renderTypeScriptLoader(config) {
  if (config.template === "set-based") {
    return renderSetBasedTypeScriptLoader(config);
  }
  return `import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

/**
 * This file is auto-generated by scripts/gen-package-runtime-loaders.mjs.
 * Do not edit directly.
 */
const PM_PACKAGE_ROOT_ENV = "PM_CLI_PACKAGE_ROOT";
const CURRENT_EXTENSION_ROOT = path.dirname(fileURLToPath(import.meta.url));
const EXTENSION_NAME = "${config.extensionName}";
const PACKAGE_NAME = "${config.packageName}";
const DIAGNOSTIC_NAME = "${config.diagnosticName}";
export type PackageRuntimeModule = Record<string, unknown>;

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null;
}

function isMissingRuntimeModuleError(error: unknown, modulePath: string): boolean {
  if (!isRecord(error) || error.code !== "ERR_MODULE_NOT_FOUND") {
    return false;
  }
  const moduleUrl = pathToFileURL(modulePath).href;
  if (error.url === moduleUrl) {
    return true;
  }
  if (typeof error.path === "string" && path.resolve(error.path) === path.resolve(modulePath)) {
    return true;
  }
  const message = typeof error.message === "string" ? error.message : "";
  const normalizedModulePath = modulePath.replace(/\\\\/g, "/");
  return (
    message.startsWith(\`Cannot find module '\${modulePath}'\`) ||
    message.startsWith(\`Cannot find module '\${normalizedModulePath}'\`) ||
    message.startsWith(\`Cannot find module '\${moduleUrl}'\`)
  );
}

function resolvePackageRootCandidates(): string[] {
  const envRoot = process.env[PM_PACKAGE_ROOT_ENV];
  const candidates = typeof envRoot === "string" && envRoot.trim().length > 0 ? [path.resolve(envRoot.trim())] : [];
  const argvEntry = typeof process.argv[1] === "string" ? process.argv[1].trim() : "";
  if (argvEntry.length > 0) {
    const entryDir = path.dirname(path.resolve(argvEntry));
    candidates.push(path.resolve(entryDir, ".."));
    candidates.push(path.resolve(entryDir, "../.."));
    candidates.push(path.resolve(entryDir, "../../.."));
  }
  return [...new Set(candidates)];
}

function runtimePaths(packageRoot: string): string[] {
  return [
    path.join(packageRoot, ".agents", "pm", "extensions", EXTENSION_NAME, "runtime.ts"),
    path.join(packageRoot, "packages", PACKAGE_NAME, "extensions", EXTENSION_NAME, "runtime.ts"),
  ];
}

async function tryRuntime(modulePath: string, attempted: string[]): Promise<PackageRuntimeModule | undefined> {
  attempted.push(modulePath);
  if (!existsSync(modulePath)) {
    return undefined;
  }
  try {
    return await import(pathToFileURL(modulePath).href) as PackageRuntimeModule;
  } catch (error: unknown) {
    if (isMissingRuntimeModuleError(error, modulePath)) {
      return undefined;
    }
    throw error;
  }
}

export async function loadPackageRuntimeModule(): Promise<PackageRuntimeModule> {
  const attempted: string[] = [];
  for (const modulePath of [...resolvePackageRootCandidates().flatMap(runtimePaths), path.join(CURRENT_EXTENSION_ROOT, "runtime.ts")]) {
    const loaded = await tryRuntime(modulePath, attempted);
    if (loaded) {
      return loaded;
    }
  }
  throw new Error(
    \`Unable to resolve packaged \${DIAGNOSTIC_NAME} extension runtime module. \` +
      \`Tried: \${attempted.join(", ")}. Ensure the installed extension includes runtime.ts or PM_CLI_PACKAGE_ROOT points to an installed pm package root.\`,
  );
}
`;
}

function renderSetBasedTypeScriptLoader(config) {
  return `import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

/**
 * This file is auto-generated by scripts/gen-package-runtime-loaders.mjs.
 * Do not edit directly.
 */
const PM_PACKAGE_ROOT_ENV = "PM_CLI_PACKAGE_ROOT";
const localRuntime = path.join(path.dirname(fileURLToPath(import.meta.url)), "runtime.ts");
export type PackageRuntimeModule = Record<string, unknown>;
const packageConfig = { extensionName: "${config.extensionName}", packageName: "${config.packageName}", diagnosticName: "${config.diagnosticName}" } as const;

const runtimeRoots = (): string[] => {
  const argvRoot = process.argv[1]?.trim()
    ? ["..", "../..", "../../.."].map((parent) => path.resolve(path.dirname(path.resolve(process.argv[1])), parent))
    : [];
  const candidates = [process.env[PM_PACKAGE_ROOT_ENV]?.trim(), ...argvRoot]
    .filter((root): root is string => Boolean(root))
    .map((root) => path.resolve(root));
  return [...new Set(candidates)];
};

const runtimeFiles = (): string[] => [
  ...runtimeRoots().flatMap((root) => [
    path.join(root, ".agents", "pm", "extensions", packageConfig.extensionName, "runtime.ts"),
    path.join(root, "packages", packageConfig.packageName, "extensions", packageConfig.extensionName, "runtime.ts"),
  ]),
  localRuntime,
];

const runtimeRecord = (value: unknown): Record<string, unknown> | undefined =>
  typeof value === "object" && value !== null ? value as Record<string, unknown> : undefined;

const isTargetMissing = (error: unknown, target: string): boolean => {
  const record = runtimeRecord(error);
  const targetUrl = pathToFileURL(target).href;
  const message = typeof record?.message === "string" ? record.message : "";
  return record?.code === "ERR_MODULE_NOT_FOUND" &&
    (record.url === targetUrl ||
      (typeof record.path === "string" && path.resolve(record.path) === path.resolve(target)) ||
      [target, target.replace(/\\\\/g, "/"), targetUrl].some((value) => message.startsWith(\`Cannot find module '\${value}'\`)));
};

const loadRuntimeFile = async (target: string, attempted: string[]): Promise<PackageRuntimeModule | undefined> => {
  attempted.push(target);
  if (!existsSync(target)) {
    return undefined;
  }
  try {
    return await import(pathToFileURL(target).href) as PackageRuntimeModule;
  } catch (error: unknown) {
    if (isTargetMissing(error, target)) {
      return undefined;
    }
    throw error;
  }
};

export async function loadPackageRuntimeModule(): Promise<PackageRuntimeModule> {
  const attempted: string[] = [];
  for (const target of runtimeFiles()) {
    const loaded = await loadRuntimeFile(target, attempted);
    if (loaded !== undefined) {
      return loaded;
    }
  }
  throw new Error(
    \`Unable to resolve packaged \${packageConfig.diagnosticName} extension runtime module. \` +
      \`Tried: \${attempted.join(", ")}. Ensure the installed extension includes runtime.ts or PM_CLI_PACKAGE_ROOT points to an installed pm package root.\`,
  );
}
`;
}

function normalizeGeneratedText(content) {
  return content.replace(/\r\n/g, "\n");
}

export async function main() {
  const check = process.argv.includes("--check");
  let drift = false;

  for (const config of PACKAGES) {
    const outputs = [
      {
        target: path.join(config.targetDirectory, "runtime-loader.ts"),
        content: renderTypeScriptLoader(config),
      },
    ];

    for (const output of outputs) {
      if (check) {
        let current = "";
        try {
          current = await readFile(output.target, "utf8");
        } catch {
          current = "";
        }
        if (normalizeGeneratedText(current) !== normalizeGeneratedText(output.content)) {
          drift = true;
          console.error(`Out of sync: ${path.relative(repoRoot, output.target)}`);
        }
      } else {
        await writeFile(output.target, output.content);
        console.log(`Wrote ${path.relative(repoRoot, output.target)}`);
      }
    }
  }

  if (check && drift) {
    console.error("Run: node scripts/gen-package-runtime-loaders.mjs");
    process.exit(1);
  }
  if (check) {
    console.log("Package runtime loaders in sync.");
  }
}

/* c8 ignore start -- CLI auto-run guard; logic covered via exported main() */
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
/* c8 ignore stop */
