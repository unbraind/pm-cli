#!/usr/bin/env node
/**
 * Generator for the plugin pm-mcp-server.mjs launchers.
 *
 * CONSTRAINT: plugins/pm-claude/scripts/pm-mcp-server.mjs and
 * plugins/pm-codex/scripts/pm-mcp-server.mjs are SHIPPED inside the plugin
 * directories and are executed standalone by the host (Claude Code / Codex)
 * relative to the plugin root (see each plugin's .mcp.json). They therefore
 * MUST NOT import any module outside their own plugin directory — a runtime
 * shared import would break a cached/standalone plugin install.
 *
 * To deduplicate without breaking standalone install, both wrappers are
 * generated from the single template below. Run this script after editing the
 * template; it overwrites both wrappers and reports drift via --check.
 *
 * Usage:
 *   node scripts/gen-plugin-mcp-wrappers.mjs          # write both wrappers
 *   node scripts/gen-plugin-mcp-wrappers.mjs --check  # fail if out of sync
 */
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const TARGETS = [
  {
    style: "direct",
    target: path.join(repoRoot, "plugins", "pm-claude", "scripts", "pm-mcp-server.mjs"),
  },
  {
    style: "loop",
    target: path.join(repoRoot, "plugins", "pm-codex", "scripts", "pm-mcp-server.mjs"),
  },
];

/**
 * The single source of truth for both plugin MCP launchers.
 *
 * Resolution order:
 * 1. PM_CLI_MCP_SERVER env var (explicit path)
 * 2. dist/mcp/server.js walking up from this file (repo checkout)
 * 3. npx -y --package=@unbrained/pm-cli@latest pm-mcp (installed via npm)
 */
function renderWrapper(style = "direct") {
  if (style === "loop") {
    return renderLoopWrapper();
  }
  return `#!/usr/bin/env node
/**
 * This file is auto-generated by scripts/gen-plugin-mcp-wrappers.mjs.
 * Do not edit directly.
 */
import { spawn } from "node:child_process";
import { access } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
const here = path.dirname(fileURLToPath(import.meta.url));

async function exists(target) {
  try { await access(target); return true; } catch { return false; }
}
function isImportUrl(target) {
  if (/^[A-Za-z]:[\\\\/]/.test(target)) {
    return false;
  }
  try {
    return new URL(target).protocol.length > 0;
  } catch {
    return false;
  }
}
function isMissingServerModule(error) {
  return typeof error === "object" && error !== null && error.code === "ERR_MODULE_NOT_FOUND";
}
async function importServerModule(target) {
  try {
    return await import(target);
  } catch (error) {
    if (isMissingServerModule(error)) {
      return null;
    }
    throw error;
  }
}
async function findRepoServer() {
  let cursor = here;
  for (let depth = 0; depth < 10; depth += 1) {
    const candidate = path.join(cursor, "dist", "mcp", "server.js");
    if (await exists(candidate)) {
      return candidate;
    }
    const parent = path.dirname(cursor);
    if (parent === cursor) {
      break;
    }
    cursor = parent;
  }
  return null;
}
async function startServer(target) {
  if (!target) {
    return false;
  }
  if (isImportUrl(target)) {
    const server = await importServerModule(target);
    if (!server) {
      return false;
    }
    server.startMcpServer();
    return true;
  }
  if (!(await exists(target))) {
    return false;
  }
  const server = await importServerModule(pathToFileURL(path.resolve(target)).href);
  if (!server) {
    return false;
  }
  server.startMcpServer();
  return true;
}
if (!(await startServer(process.env.PM_CLI_MCP_SERVER)) && !(await startServer(await findRepoServer()))) {
  const child = spawn("npx", ["-y", "--package=@unbrained/pm-cli@latest", "pm-mcp"], { stdio: "inherit", env: process.env });
  child.on("exit", (code, signal) => {
    if (signal) {
      process.kill(process.pid, signal);
      return;
    }
    process.exit(code ?? 1);
  });
}
`;
}

function renderLoopWrapper() {
  return `#!/usr/bin/env node
/**
 * This file is auto-generated by scripts/gen-plugin-mcp-wrappers.mjs.
 * Do not edit directly.
 */
import { spawn } from "node:child_process";
import { access } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

async function canRead(target) {
  try {
    await access(target);
    return true;
  } catch {
    return false;
  }
}

function hasImportProtocol(target) {
  if (/^[A-Za-z]:[\\\\/]/.test(target)) {
    return false;
  }
  try {
    return new URL(target).protocol.length > 0;
  } catch {
    return false;
  }
}

function isMissingLaunchModule(error) {
  return typeof error === "object" && error !== null && error.code === "ERR_MODULE_NOT_FOUND";
}

async function importLaunchModule(target) {
  try {
    return await import(target);
  } catch (error) {
    if (isMissingLaunchModule(error)) {
      return null;
    }
    throw error;
  }
}

async function locateRepoServer() {
  let cursor = path.dirname(fileURLToPath(import.meta.url));
  for (let depth = 0; depth < 10; depth += 1) {
    const candidate = path.join(cursor, "dist", "mcp", "server.js");
    if (await canRead(candidate)) {
      return candidate;
    }
    const parent = path.dirname(cursor);
    if (parent === cursor) {
      return null;
    }
    cursor = parent;
  }
  return null;
}

async function launchServer(target) {
  if (!target) {
    return false;
  }
  if (hasImportProtocol(target)) {
    const server = await importLaunchModule(target);
    if (!server) {
      return false;
    }
    server.startMcpServer();
    return true;
  }
  if (!(await canRead(target))) {
    return false;
  }
  const server = await importLaunchModule(pathToFileURL(path.resolve(target)).href);
  if (!server) {
    return false;
  }
  server.startMcpServer();
  return true;
}

if (!(await launchServer(process.env.PM_CLI_MCP_SERVER)) && !(await launchServer(await locateRepoServer()))) {
  const child = spawn("npx", ["-y", "--package=@unbrained/pm-cli@latest", "pm-mcp"], { stdio: "inherit", env: process.env });
  child.on("exit", (code, signal) => {
    if (signal) {
      process.kill(process.pid, signal);
      return;
    }
    process.exit(code ?? 1);
  });
}
`;
}

export async function main() {
  const check = process.argv.includes("--check");
  let drift = false;
  for (const { style, target } of TARGETS) {
    const content = renderWrapper(style);
    if (check) {
      let current = "";
      try {
        current = await readFile(target, "utf-8");
      } catch {
        current = "";
      }
      if (current !== content) {
        drift = true;
        console.error(`Out of sync: ${path.relative(repoRoot, target)}`);
      }
    } else {
      await writeFile(target, content);
      console.log(`Wrote ${path.relative(repoRoot, target)}`);
    }
  }
  if (check && drift) {
    console.error("Run: node scripts/gen-plugin-mcp-wrappers.mjs");
    process.exit(1);
  }
  if (check) {
    console.log("Plugin MCP wrappers in sync.");
  }
}

/* c8 ignore start -- CLI auto-run guard; logic covered via exported main() */
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main().catch((err) => {
    console.error(err);
    process.exit(1);
  });
}
/* c8 ignore stop */
